    func toggleSelection(for option: SoundOption) {
        if let index = soundOptions.firstIndex(where: { $0.id == option.id }) {
            soundOptions[index].isSelected.toggle()
            print(soundOptions[index])
            saveSoundOptions()
        }
    }
    
    func selectAll() {
        var bl = true
        for index in soundOptions.indices {
            if(!soundOptions[index].isSelected) {
                bl = false
            }
            soundOptions[index].isSelected = true
        }
        if(bl) {
            for index in soundOptions.indices {
                soundOptions[index].isSelected = false
            }
        }
        saveSoundOptions()
    }
    
    func searchSoundOption(by name: String) -> SoundOption? {
        return soundOptions.first(where: { $0.name.lowercased() == name.lowercased() })
